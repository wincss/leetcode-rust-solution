use crate::*;

#[test]
fn case_1() {
    assert_eq!(
        Solution::median_sliding_window(vec![1, 3, -1, -3, 5, 3, 6, 7], 3),
        vec![1.0, -1.0, -1.0, 3.0, 5.0, 6.0]
    );
}

#[test]
fn case_2() {
    assert_eq!(
        Solution::median_sliding_window(vec![1, 2, 3, 4, 2, 3, 1, 4, 2], 3),
        vec![2.0, 3.0, 3.0, 3.0, 2.0, 3.0, 2.0]
    );
}
#[test]
fn case_3() {
    assert_eq!(
        Solution::median_sliding_window(vec![2147483647, 2147483647], 2),
        vec![2147483647.0]
    );
}

#[test]
fn case_4() {
    assert_eq!(
        Solution::median_sliding_window(vec![2147483647, 1, 2, 3, 4, 5, 6, 7, 2147483647], 2),
        [1073741824.0, 1.5, 2.5, 3.5, 4.5, 5.5, 6.5, 1073741827.0]
    );
}
