use crate::*;

fn convert_sv(board: [[&str; 9]; 9]) -> Vec<Vec<char>> {
    board
        .iter()
        .map(|v| {
            v.iter()
                .map(|s| {
                    let mut c = '.';
                    for i in s.chars() {
                        c = i;
                    }
                    c
                })
                .collect()
        })
        .collect()
}

#[test]
fn case_1() {
    assert!(Solution::is_valid_sudoku(convert_sv([
        ["5", "3", ".", ".", "7", ".", ".", ".", "."],
        ["6", ".", ".", "1", "9", "5", ".", ".", "."],
        [".", "9", "8", ".", ".", ".", ".", "6", "."],
        ["8", ".", ".", ".", "6", ".", ".", ".", "3"],
        ["4", ".", ".", "8", ".", "3", ".", ".", "1"],
        ["7", ".", ".", ".", "2", ".", ".", ".", "6"],
        [".", "6", ".", ".", ".", ".", "2", "8", "."],
        [".", ".", ".", "4", "1", "9", ".", ".", "5"],
        [".", ".", ".", ".", "8", ".", ".", "7", "9"]
    ])));
}

